package com.example.gps_g11.Articles;

public class ArticleItem {
    private String title;
    private String link;

    public ArticleItem(String title, String link) {
        this.title = title;
        this.link = link;
    }

    public String getTitle() {
        return title;
    }

    public String getLink() {
        return link;
    }
}
